public class PriceModel {

	String priceMorning;
	String priceAfternoon;
	String priceEvening;
	/**
	 * @return the priceMorning
	 */
	public String getPriceMorning() {
		return priceMorning;
	}
	/**
	 * @param priceMorning the priceMorning to set
	 */
	public void setPriceMorning(String priceMorning) {
		this.priceMorning = priceMorning;
	}
	/**
	 * @return the priceAfternoon
	 */
	public String getPriceAfternoon() {
		return priceAfternoon;
	}
	/**
	 * @param priceAfternoon the priceAfternoon to set
	 */
	public void setPriceAfternoon(String priceAfternoon) {
		this.priceAfternoon = priceAfternoon;
	}
	/**
	 * @return the priceEvening
	 */
	public String getPriceEvening() {
		return priceEvening;
	}
	/**
	 * @param priceEvening the priceEvening to set
	 */
	public void setPriceEvening(String priceEvening) {
		this.priceEvening = priceEvening;
	}
	
	

}
