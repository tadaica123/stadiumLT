package com.leontran.stadiumlt.model;

public class Folder {
	String image;
	String file_name;
	String file_location;
	String count_image;
	String date_time;
	boolean check;
	/**
	 * @return the check
	 */
	public boolean isCheck() {
		return check;
	}
	/**
	 * @param check the check to set
	 */
	public void setCheck(boolean check) {
		this.check = check;
	}
	/**
	 * @return the image
	 */
	public String getImage() {
		return image;
	}
	/**
	 * @param image the image to set
	 */
	public void setImage(String image) {
		this.image = image;
	}
	/**
	 * @return the file_name
	 */
	public String getFile_name() {
		return file_name;
	}
	/**
	 * @param file_name the file_name to set
	 */
	public void setFile_name(String file_name) {
		this.file_name = file_name;
	}
	/**
	 * @return the count_image
	 */
	public String getCount_image() {
		return count_image;
	}
	/**
	 * @param count_image the count_image to set
	 */
	public void setCount_image(String count_image) {
		this.count_image = count_image;
	}
	/**
	 * @return the file_location
	 */
	public String getFile_location() {
		return file_location;
	}
	/**
	 * @param file_location the file_location to set
	 */
	public void setFile_location(String file_location) {
		this.file_location = file_location;
	}
	/**
	 * @return the date_time
	 */
	public String getDate_time() {
		return date_time;
	}
	/**
	 * @param date_time the date_time to set
	 */
	public void setDate_time(String date_time) {
		this.date_time = date_time;
	}
	
	
	
}
